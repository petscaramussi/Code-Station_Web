<h2>A brincadeira da sintaxe</h2>
<h3>- Cada instrução deve terminar com ponto-e-vírgula.</h3>
<div class="codigo-java">
x = x + 1

</div>
<h3>- Um comentário de linha única começa com duas barras.</h3>
<div class="codigo-java">
x = 22;

// esta linha me incomoda
</div>
<h3>- A maioria dos espaços em branco não é importante</h3>
<div class="codigo-java">
x     =     3     ;

</div>
<h3>- As variáveis são declaradas com um nome e um tipo (você aprenderá todos os tipos Java no Capítulo 3).</h3>
<div class="codigo-java">
<pre>
int CodeStation;

// tipo: int, nome: CodeStation
</pre>
</div>
<h3>- As classes e métodos devem ser definidos dentro de uma par de chaves.</h3>
<div class="codigo-java">
<pre>
public void go() { 
// o código entra aqui 
}
</pre>
</div>
