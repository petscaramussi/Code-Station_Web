<h2>Estrutura do código</h2>

<h3>O que existe em um arquivo-FONTE?</h3>
<p>Um arquivo de código-fonte (com a extensão Java) contém uma definição de classe. A classe representa uma parte do seu programa, embora um aplicativo muito pequeno possa precisar apenas de uma classe. A classe deve ficar dentro de um par de chaves</p>

<div class="codigo-java">
<pre>
public class Code Station { 

}
</pre>
</div>
<h3>O que existe em uma CLASSE?</h3>
<p>Uma classe tem um ou mais métodos. Na classe Code Station, o método programar conterá instruções de como o cão deve latir. Seus métodos devem ser declarados denro de uma classe (em outras palavras, dentro das chaves da classe).</p>

<div class="codigo-java">
<pre>
public class Code Station {
 void programar() {

 }
}
</pre>
</div>

<h3>O que existe em um MÉTODO?</h3>
<p>Dentro das chaves de um método, escreva as instruções de como ele deve ser executado. O código do método é basicamente um conjunto de instruções, e por enquanto você pode considerar o método como se fosse uma função ou procedimento.</p>
<div class="codigo-java">
<pre>
public class Code Station {
 void programar() {
  instrução1;
  instrução2;
 }
}
</pre>
</div>