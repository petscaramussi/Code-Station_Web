
<h3> Um objeto seria como um registro de sua agenda de endereços. </h3>
<p>Uma analogia que poderíamos usar para os objetos seriam um conjunto de fichas Rolodex não utilizadas.
Todas as fichas têm os mesmos campos em branco (as variáveis de instância).
Quando você preencher uma ficha, estará criando uma instância (objeto), e as entradas
que criar nessa ficha representarão seu estado.</p>

<p>Os métodos da classe são as coisas que você pode fazer com uma ficha específica: obterNome(), alterarNome(), configurarNome(), todos poderiam ser métodos da classe Rolodex</p>

<p>Portanto, todas as fichas fazem as mesmas coisas (obterNome(), alterarNome(), etec.), mas cada um conhece coisas exclusivas sobre si mesma.</p>
