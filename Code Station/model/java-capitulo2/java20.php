<h3> Rápido! Saia de main! </h3>
<p>
Se você estiver em main(), não estará realmente em Objetópolis.
É adequada um programa de teste ser executado dentro do método main, em um aplicativo OO real, 
você precisará de objetos que se comuniquem com outros objetos, e não de um métodos main() estático criando e testando objetos
</p>

<h3>As duas finalidades de main: </h3>

<p>- Testar sua classe real</p>
<p>- Acionar/iniciar seu aplicativo Java</p>

<p>
Um aplicativo Java real nada mais é do que objetos se comunicando com outros objetos.
Nesse caso, comunicar-se significa os objetos chamando os métodos uns dos outros. 
Na página anterior, e no Capítulo 4, examinamos o uso de um método main() em uma classe TestDrive separada 
para criar e testar os métodos e variáveis de outra classe. 
No capítulo 6 examinaremos o uso de uma classe com um método main() para iniciar um aplicativo Java real
(criando objetos e, em seguida, deixando-os livres para interagir com outros objetos, etc.)
</p>
