
<h2>Uma referência de objeto é apenas outro valor da variável.</h2>
<br>

<p>Algo que é despejado em uma xícara. Só que dessa vez é um controle remoto.</p>

<h3>Variável primitiva.</h3>
<div class="codigo-java">
<pre>
byte x = 7;
</pre>
</div>
<p>Os bits que representam 7 estão na variável (00000111).</p>
<h3>Variável de referência</h3>
<div class="codigo-java">
<pre>
Dog myDog = new Dog( );
</pre>
</div>
<p>Os bits que representam uma maneira de acessar o objeto Dog ficam dentro da variável.
O objeto Dog propriamente dito não fica na variável!</p>
