
<h2> As 3 etapas de declaração, criação e atribuição de objetos </h2>

<p>Dog myDog = new Dog();</p>

<h3> 1. Declare uma variável de referência </h3>
<br>
<div class="codigo-java">
<pre>
Dog myDog = new Dog();
</pre>
</div>

<p>Solicita a JVM para alocar espaço para uma variável de referência e nomeia essa variável como myDog. 
A variável de referência será sempre do tipo Dog. Em outras palavras, um controle remoto que tenha botões que
controlam um objeto Dog, mas não um objeto Car, Button ou Socket.</p>
<br>
<h3> 2. Crie um objeto </h3>
<br>

<div class="codigo-java">
<pre>
Dog myDog = new Dog( );
</pre>
</div>
<p>Solicita a JVM para alocar espaço para um novo objeto Dog no heap (aprendermos muito mais sobre processo). Objeto Dog</p>
<br>
<h3> 3. Vincule o objeto e a referência </h3>
<br>

<div class="codigo-java">
<pre>
Dog myDog = new Dog( );
</pre>
</div>
<p>Atribui o novo objeto Dog a variável de referência myDog. Em outras palavras, programa o controle remoto</p>

