
<h2>"Gostaria de um café duplo, não traga um do tipo inteiro."</h2>

<p>Quando você pensar em variáveis Java, pense em xícaras de café, xícaras de chá,
canecas gigantes onde cabe muita cerveja, esses grandes copos em que as pipocas 
são vendidas no cinema, xícaras com alças curvilíneas e sexy e 
canecas com acabamento metálico que lhe disseram para nunca colocar em 
um micro-ondas.</p>

<h2>Uma variável é apenas uma xícara. Um contêiner. Ele contém algo.</h2>

<p>Ele tem um tamanho e um tipo. Neste capítulo, examinaremos primeiro as variáveis
(xícaras) que contêm tipos primitivos e, um pouco mais adiante, discutiremos as
xícaras que contêm referências a objetos. Não deixe de acompanhar toda a nossa 
analogia com as xícaras - tão simples como está sendo agora, ela nos fornecerá 
uma maneira comum de examinar as coisas quando a discussão ficar mais complexa.
É isso ocorrerá em breve.</p>

<p>As variáveis primitivas são como as xícaras que vemos nos cafés. 
Se você já foi a um Starbucks aos Estados Unidos, sabe sobre o que estamos
falando aqui. Elas têm tamanhos diferentes e cada uma tem um nome como 
"pequena”, “grande" ou "Gostaria de um “moca” grande com pouca cafeína e 
chantilly".</p>

<p>E, em Java, existem tamanhos diferentes para as variáveis primitivas e esse
tamanho têm nomes. Quando você declarar uma variável em Java, deve declará-la 
com um tipo específico. Os quatros contêineres mostrados aqui são para os quatro
tipos primitivos inteiros em Java.</p>

<p>Cada xícara contém uma quantidade, o mesmo ocorrendo com as variáveis 
primitivas Java. De modo que, em vez de dizer "Quero uma xícara grande de café
francês torrado", você diria ao compilador "Quero uma variável int com o número 
90, por favor". Exceto por uma pequena diferença... Em Java, você também terá
que fornecer um nome para sua xícara. Portanto, na verdade diríamos "Quero um 
int, por favor, com o valor 2.486 e chame a variável de tamanho". 
Cada variável primitiva possui uma quantidade fixa de bits (tamanho da xícara). 
Os tamanhos das seis variáveis primitivas numéricas em Java são mostrados a seguir.</p>
