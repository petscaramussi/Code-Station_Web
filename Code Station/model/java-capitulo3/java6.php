
<h3>Você pode atribuir um valor a uma variável de várias maneiras dentre elas: </h3>
	<p>- digitar um valor literal depois do sinal de igualdade (x = 12, isGod = true, etc).</p>
	<p>- atribuir o valor de uma variável a outra (x = y)</p>
	<p>- usar uma expressão combinando os dois (x = y + 43)</p>
<h2>Nos exemplos a seguir, os valores literais estão em itálico e negrito:</h2>

<div class="codigo-java">
<p>int size = 32; declara um int chamado size e atribui a ele o valor de 32</p>

<p>char initial = 'j';  declara um char chamado initial e atribui a ele o valor 'j';</p>

<p>double d = 456,709;</p>

<p>boolean isCrazy; declara um booleano chamado isCrazy (sem atribuição)</p>

<p>int y = x + 456; declara um int chamado y e atribui a ele um valor que é igual á soma do valor atual de x mais 456</p>
</div>
