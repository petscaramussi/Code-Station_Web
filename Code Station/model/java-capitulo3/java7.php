<h2>Afaste-se dessa palavra-chave!</h2>

<p>Você sabe que precisa de um nome e de um tipo para suas variáveis.
Você já conhece os tipos primitivos.

Mas o que pode usar como nomes? As regras são simples. Você pode nomear uma classe, método ou variável de acordo com as regras as seguir(as regras reais são um pouco mais flexíveis, mas estas o manterão em segurança):

<p>- Ele deve começar com uma letra, um sublinhado(_) ou o cifrão($).</p>
Você não pode iniciar um nome com um número.

<p>- Depois do primeiro caractere, você também pode usar números. Só não comece com um número.</p>

<p>- O nome pode ser o que você quiser, se obedecer as regras, contanto que não seja uma das palavras reservadas do Java.</p>

<p>As palavras reservadas são palavras-chave (e outras coisas) que o compilador reconhece. Mas se você quiser realmente brincar de confundir o compilador, simplesmente tente usar uma palavra reservada como um nome.</p>

<p>Você já viu algumas palavras reservadas quando examinamos a criação de nossa primeira classe principal:</p>

<p>public static void < não use nenhuma dessas palavras nos nomes que criar.</p>

<h2>E os tipos primitivos também são reservados:</h2>

<p>boolean char byte short int long float double < não use nenhuma dessas palavras nos nomes que criar.</p>

<p>Mas há muitas outras que ainda não discutimos. Mesmo se você não precisar saber o que elas significam, terá que saber que não pode usá-las em suas criações. Não tente - de forma alguma - memorizá-las agora. Para reservar espaço em sua mente, provavelmente você teria que perder alguma outra coisa. Como onde seu carro está estacionado. Não se preocupe, até o fim do livro você terá a maioria delas memorizada.</p>