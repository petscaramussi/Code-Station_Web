
<h2> Aponte o lápis </h2>
<br>
<p>
O compilador não deixará que você insira a quantidade de uma xícara grande em uma pequena.
Mas e quanto à operação inversa - despejar o conteúdo de uma xícara pequena em uma grande?
Sem problemas.
</p>

<p>
Baseado no que você sabe sobre o tamanho e o tipo das variáveis primitivas, veja se 
consegue descobrir quais dessas linhas são válidas e quais não são. Não abordamos todas
as regras ainda, portanto, em algumas das opções, você terá que usar o seu melhor palpite. 
Dica: sempre que o compilador erra, é em nome de segurança.
</p>
<p>
Na lista a seguir, circule as instruções que seriam válidas se essas linhas estivessem em um único método:
</p>
<div class="codigo-java">
<pre>
 1. int x = 34.5;<br>
 2. boolean boo = x;<br>
 3. int g = 17;<br>
 4. int y = g;<br>
 5. y = y + 10;<br>
 6. short s;<br>
 7. s = y;<br>
 8. byte b = 3;<br>
 9. byte v = b;<br>
 10. short n = 12;<br>
 11. v = n;<br>
 12. byte k = 128;<br>
</pre>
</div>
