
<p>Você usará o operador ponto(.) em uma variável de referência para dizer "use o que está
antes do ponto para me trazer o que está do ponto". Por exemplo:</p>
<div class="codigo-java">
<pre>
myDog.bark( );
</pre>
</div>
<p>Significa "use o objeto referenciado pela variável myDog para chamar o método bark()". 
Quando você usar o operador ponto em uma variável de referência de objeto, considere isso 
como se estivesse pressionado um botão do controle remoto desse objeto.
</p>
<div class="codigo-java">
<pre>
	Dog d = new Dog( );
	d.bark( );
</pre>
</div>
